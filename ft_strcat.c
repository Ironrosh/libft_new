char	*ft_strcat(char *dest, const char *src)
{
	int		i;
	int		l;

	i = 0;
	while (dest[i])
		i++;
	l = 0;
	while (src[l])
	{
		dest[i] = src[l];
		i++;
		l++;
	}
	dest[i] = '\0';
	return (dest);
}
